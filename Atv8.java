public class Atv8 {
    public static void main(String args[]) {

        int[] vet = new int[50];

        for(int i = 0; i < 50; i++){
            vet[i] = 0;
        }

        for(int i = 0; i < 50; i++){
            System.out.println(vet[i] + "\n");
        }
    }
}
